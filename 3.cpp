#include <iostream>

using namespace std;

int main()
{
	setlocale(0, "");

	double num = 0;
	double years = 0;

	cout << "Сколько денег вы хотите положить в сберкассу?: ";
	cin >> num;

	cout << "Через сколько лет забрать вклад?: ";
	cin >> years;

	cout << "Ответ будет: " << num * (1 + (years * 0.03));
}

